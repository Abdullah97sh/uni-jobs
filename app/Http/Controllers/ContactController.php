    public function storeContactForm(Request $request)
    {
        // dd($request->all());
        $request->validate([
            'first_name' => 'required',
            'last_name' => 'required',
            'email' => 'required|email',
            'subject' => 'required',
            'message' => 'required',
        ]);

        $input = $request->all();
        Contact::create($input);

        return redirect()->back()->with(['success' => 'Contact Form Submit Successfully']);
    }
